#Declarando uma lista com times de futebol
times = ["Vasco","CRB","Flamengo","ASA","Botafogo-PB","Real Madrid",
         "Barcelona", "Liverpool","Chivas", "Colo Colo", "Peñarol", "Defensia Y Justicia"]

#Printando o número de elementos presentes na lista de times
print(len(times))

